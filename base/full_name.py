# Créer les 2 variables:
prenom = "ALEX"
nom = "lamizana"

# Affiche le nom et prénom séparemments:
print(prenom, nom)
print(f"{prenom} {nom}")

# Met tout en minuscule:
print(prenom.lower(), nom.lower())

# Met juste le nom en majuscule:    
print(prenom.lower(), nom.upper())
      